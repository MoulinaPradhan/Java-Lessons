package src.codechef;
import java.util.*;



public class ColdPlay {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t= sc.nextInt();
        while(t --> 0){
            int M = sc.nextInt();
            int S = sc.nextInt();
            System.out.println(M/S);
        }
    }
}
